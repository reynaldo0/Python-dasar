# Tipe data Integer / Data Dengan Angka
integer = 15

print("Data Ini bernilai : ", integer)
print ("Yeng Bertipe data : ", type(integer))

# Tipe data Float / Angka Dengan Koma
float = 50.5

print("Data Ini bernilai : ", float)
print ("Yeng Bertipe data : ", type(float))

# Tipe data String / Kumpulan Karakter
string = "Hallo Guys"

print("Data Ini bernilai : ", string)
print ("Yeng Bertipe data : ", type(string))

# Tipe data Boolean / Biner
bool = True

print("Data Ini bernilai : ", bool)
print ("Yeng Bertipe data : ", type(bool))

# Tipe Data Khusus

# Bilangan Kompleks
complex = complex(10,5)
print("Data Ini bernilai : ", complex)
print ("Yeng Bertipe data : ", type(complex))

# Tipe Data Dari Bahasa C
from ctypes import c_double
c_double = c_double(70.5)
print("Data Ini bernilai : ", c_double)
print ("Yeng Bertipe data : ", type(c_double))