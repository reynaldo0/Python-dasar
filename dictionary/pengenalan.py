# List = > array, mengakses data dengan menggunakan index

data_list = ["Ucup","Otong", "Asep"]
print(data_list[0])

# Dictionary (Dict) -> associative array 
# Identifier -> key

data_dict = {
    "key":"value",
    "up":"ucup",
    "og":"Otong",
    "ap":"Asep",
    "num":100,
    "list":data_list,
}

print(data_dict["og"])
print(data_dict["num"])
print(data_dict["list"])